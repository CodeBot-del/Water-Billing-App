    {
        //dd($request->all());  //render the requests array on the screen
        $path = $request->image->store('public/customer');
        Customer::create([
            'name' => $request->name,
            'email' => $request->email,
            'phone' => $request->phone,
            'faculty' => $request->faculty,
            'category' => $request->category,
            'image' => $path,
        ]);
        return redirect()->route('customer.index');

